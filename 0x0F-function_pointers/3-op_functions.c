#include "3-calc.h"

/**
 * op_add - A funtion that adds two integers
 * @a: variable passed in function
 * @b: variable passed in function
 * Return: sum of the integers
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - A funtion that subtracts two integers
 * @a: variable passed in function
 * @b: variable passed in function
 * Return: 1
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - A funtion that multiplies two integers
 * @a: variable passed in function
 * @b: variable passed in function
 * Return: product of the integers
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - A function that divide two integers
 * @a: variable passed in function
 * @b: variable passed in function
 * Return: 1
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - A function that gets the modulus of integers
 * @a: variable passed in function
 * @b: variable passed in function
 * Return: 1
 */

int op_mod(int a, int b)
{
return (a % b);
}
